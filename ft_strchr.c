#include "libft.h"

char		*ft_strchr(const char *s, int c)
{
	int		i;

	i = 0;
	if (!s)
		return (NULL);
	while (s[i] && s[i] != (char)c)
		i++;
	if (s[i] || (c == 0 && s[i] == 0))
		return (s + i);
	return (NULL);
}
